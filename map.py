class Map:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.world_map = {}
        self.get_map()

    def get_map(self):
        tmx_file = "map/NEW MAP.tmx"  # Замените на путь к вашему TMX файлу
        tmx_data = pytmx.load_pygame(tmx_file)
        for layer in tmx_data.visible_layers:
           if isinstance(layer, pytmx.TiledTileLayer):
               for x, y, gid in layer:
                   tile = tmx_data.get_tile_image_by_gid(gid)
                   if tile:
                       self.game.bg_img.blit(tile, (x * tmx_data.tilewidth, y * tmx_data.tileheight))

    def draw(self):
        [pygame.draw.rect(self.game.bg_img, 'blue', (pos[0] * 100, pos[1] * 100, 100, 100), 2)
         for pos in self.world_map]